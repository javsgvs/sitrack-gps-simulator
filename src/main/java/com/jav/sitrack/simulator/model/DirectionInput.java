package com.jav.sitrack.simulator.model;

public class DirectionInput {

	private String from;
	private String to;

	public DirectionInput() {
		super();
	}

	public String getFrom() {
		return from;
	}

	public void setFrom(String from) {
		this.from = from;
	}

	public String getTo() {
		return to;
	}

	public void setTo(String to) {
		this.to = to;
	}

}

